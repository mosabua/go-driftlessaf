package main

import (
	"context"
	"errors"
	"fmt"
	"strings"
	"text/template"

	"chainguard.dev/driftlessaf/agents/metaagent"
	"chainguard.dev/driftlessaf/agents/toolcall/callbacks"
	"chainguard.dev/driftlessaf/reconcilers/githubreconciler/changemanager"
	"chainguard.dev/driftlessaf/reconcilers/githubreconciler/clonemanager"
	"chainguard.dev/driftlessaf/reconcilers/githubreconciler/metapathreconciler"
)

const (
	prTitleTemplate = `fix(modernize): apply modernize fixes to {{.Path}}`
	prBodyTemplate  = `Automated Go modernize fixes applied by {{.Identity}}.

---
*Generated by {{.Identity}}*`
)

// reconciler is a type alias for the composed metapathreconciler.
type reconciler[CB any] = metapathreconciler.Reconciler[*Request, *Result, CB]

// newReconciler constructs a reconciler from configuration.
func newReconciler[CB any](
	ctx context.Context,
	identity string,
	agent metaagent.Agent[*Request, *Result, CB],
	cloneMeta *clonemanager.Meta,
	prLabels []string,
	buildCallbacks func(context.Context, *changemanager.Session[metapathreconciler.PRData[*Request]], *clonemanager.Lease) (CB, error),
) (*reconciler[CB], error) {
	identity = strings.TrimSpace(identity)
	if identity == "" {
		return nil, errors.New("identity must be provided")
	}
	if agent == nil {
		return nil, errors.New("agent must be provided")
	}
	if cloneMeta == nil {
		return nil, errors.New("cloneMeta must be provided")
	}

	titleTmpl, err := template.New("title").Parse(prTitleTemplate)
	if err != nil {
		return nil, fmt.Errorf("parse title template: %w", err)
	}
	bodyTmpl, err := template.New("body").Parse(prBodyTemplate)
	if err != nil {
		return nil, fmt.Errorf("parse body template: %w", err)
	}

	cm, err := changemanager.New[metapathreconciler.PRData[*Request]](identity, titleTmpl, bodyTmpl,
		changemanager.WithFindingsIteration[metapathreconciler.PRData[*Request]](),
	)
	if err != nil {
		return nil, fmt.Errorf("create change manager: %w", err)
	}

	return metapathreconciler.New(ctx, identity, &goModernize{}, cm, cloneMeta, prLabels, agent,
		func(_ context.Context, findings []callbacks.Finding) (*Request, error) {
			return &Request{Findings: findings}, nil
		},
		buildCallbacks,
	)
}
